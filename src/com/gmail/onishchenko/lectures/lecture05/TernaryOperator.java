package com.gmail.onishchenko.lectures.lecture05;

public class TernaryOperator {
    public static void main(String[] args) {
        int result;
        int a = 10;
        int b = 25;

        if (a > b) {
           result = 1;
        } else {
            result = -1;
        }

        System.out.println(result);
    }
}
